    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try {
            /* TODO output your page here. You may use following sample code. */
            Account a = (Account) request.getSession().getAttribute("acc");
            if (a != null) {
                int type = a.getUserTypeID();
                switch (type) {
                    case 3:
                    case 2:
                    case 0:
                        String id = request.getParameter("id");
                        if (id == null) {
                            response.sendRedirect("library");
                        } else {
                            int bookID = -1;
                            try {
                                bookID = Integer.parseInt(id);
                            } catch (Exception e) {
                                throw new RuntimeException("Có lỗi xảy ra, vui lòng thử lại!");
                            }
                            request.setAttribute("messenger", MessageDB.getMessage(a.getUserID()));
                            Book b = BookDB.getBook(bookID);
                            request.setAttribute("book", b);
                            request.setAttribute("messenger", MessageDB.getMessage(a.getUserID()));
                            request.getRequestDispatcher("Write.jsp").include(request, response);
                        }
                        break;
                    case 1:
                        response.sendRedirect("manage");
                        break;
                }
            } else {
                response.sendRedirect("login");
            }
        } catch (IOException | ServletException e) {
            response.sendRedirect("login");
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        request.setCharacterEncoding("utf-8");
        Account a = (Account) request.getSession().getAttribute("acc");
        int bookID = Integer.parseInt(request.getParameter("bookID"));
        String idea = request.getParameter("idea");
        new Idea(a.getUserID(), bookID, idea).add();
        response.sendRedirect("read?id="+bookID);
    }

